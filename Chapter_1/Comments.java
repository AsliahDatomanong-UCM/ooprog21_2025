public class Comments {
    public static void main(String[] args) {
        
        // ---- Single-line comment ----
        // This line prints a message to the console
        System.out.println("Hello, this is a single-line comment example.");
        
        /* ---- Multi-line comment ----
           The next line of code will print another message.
           Multi-line comments are useful when writing longer explanations.
        */
        System.out.println("This is a multi-line comment example.");
        
        /**
         * ---- Documentation comment ----
         * The following line of code prints a final message.
         * Documentation comments can be extracted into HTML docs using Javadoc.
         */
        System.out.println("This is a documentation comment example.");
    }
}
